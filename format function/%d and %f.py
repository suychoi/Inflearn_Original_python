#d 
print('%d %d' % (1, 2))
print('{} {}'.format(1,2))

#4 공간 중에서 42 출력
print('%4d' % (42)) #(  42)
print('{:4d}'.format(42))   #(  42)

# %f
print('%f' % (3.1415921276))    #('3.141592')
# 정수 한자리와, 소수점 5자리까지 출력하도록 함
print('%1.5f' % (3.1415921276)) # 3.14159

print('{:f}'.format(3.1415921276))  #('3.141592')
#총 6자리 중에서 소수 2자리가 오도록
print('%06.2f' % (3.141592653589793)) #('003.14')
print('%06.2f' % (123453.141592653589793))  #123453.14
print('%06.2f' % (11123453.141592653589793))    #11123453.14
print('{:06.2f}'.format(3.141592653589793)) #003.14